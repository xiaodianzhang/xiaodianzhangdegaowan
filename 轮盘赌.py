import random
import os
import time

def 清屏():
    os.system('cls' if os.name == 'nt' else 'clear')

def 游戏():
    print("呃，这是一个我也不知道是啥，反正是个游戏的游戏")
    print("总之每次会让你选择开枪或不开")
    input("按回车继续")
    清屏()
    print("如果你选择开那么如果这个是子弹的话，你就废了")
    print("如果你选择不开,但是这回合是空的的话，会扣除你的机会\n你的机会没了你也废了")
    print("你最多连续不开5次，连续不开5次后会自动选择开")
    input("按回车开始")
    清屏()
    
    # 游戏状态变量
    被动列表 = []
    开关反转 = False
    连续不开 = 0
    最多连续不开几次 = 5
    连续不开不再受限 = False
    你共猜了 = 0
    机会 = 3
    值 = 4
    f值 = 1
    难度上升 = 0
    每次获得奖励时额外获得的机会的数量 = 0
    猜测了第几次 = 0
    猜多少次获得奖励 = 1
    免疫中弹死亡次数 = 0
    # 奖励池
    列表奖励 = ["获得一次机会","空弹的概率小幅增加","以后每次获得奖励时获得一次机会",
               "失去一次机会然后增加空弹的概率","小幅增加中弹的概率,并获得两次机会",
               "将当前的连续不开降为零","失去二次机会然后增加更多空弹概率"]
    
    # 定义奖励函数（解决 lambda 不能赋值的问题）
    def 奖励_失去二次机会然后增加空弹概率():
        nonlocal 机会,值
        机会 -= 2
        值 += 3
    def 奖励_免疫一次中弹死亡():
        nonlocal 免疫中弹死亡次数
        免疫中弹死亡次数 += 1
        
    def 奖励_获得一次机会():
        nonlocal 机会
        机会 += 1
    
    def 奖励_空弹概率更大():
        nonlocal 值
        值 += 1
    
    def 奖励_以后每次获得奖励时获得一次机会():
        nonlocal 每次获得奖励时额外获得的机会的数量
        每次获得奖励时额外获得的机会的数量 += 1
    
    def 奖励_失去一次机会然后增加空弹的概率():
        nonlocal 机会, 值
        机会 -= 1
        值 += 2
    
    def 奖励_小幅增加中弹的概率并获得两次机会():
        nonlocal 机会, f值, 值
        机会 += 2
        f值 += 1
        值 += 1
    
    def 奖励_将当前的连续不开降为零():
        nonlocal 连续不开
        连续不开 = 0
    
    def 奖励_连续不开不再受限():
        nonlocal 连续不开不再受限
        连续不开不再受限 = True
    
    def 奖励_接下来开失败了消耗机会而不开失败了游戏则失败():
        nonlocal 开关反转
        开关反转 = True
    
    # 奖励效果字典（现在存的都是函数名）
    奖励效果 = {
        "获得一次机会": 奖励_获得一次机会,
        "空弹的概率小幅增加": 奖励_空弹概率更大,
        "以后每次获得奖励时获得一次机会": 奖励_以后每次获得奖励时获得一次机会,
        "失去一次机会然后增加空弹的概率": 奖励_失去一次机会然后增加空弹的概率,
        "小幅增加中弹的概率,并获得两次机会": 奖励_小幅增加中弹的概率并获得两次机会,
        "将当前的连续不开降为零": 奖励_将当前的连续不开降为零,
        "连续不开不再受限": 奖励_连续不开不再受限,
        "接下来开失败了消耗机会而不开失败了游戏则失败": 奖励_接下来开失败了消耗机会而不开失败了游戏则失败,
        "免疫一次中弹死亡":奖励_免疫一次中弹死亡,
        "失去二次机会然后增加更多空弹概率":奖励_失去二次机会然后增加空弹概率,
    }
    
    while True:
        清屏()
        print(f"当前你共猜了{你共猜了}次")
        if 机会 <= 0:
            print("你没有机会了😵")
            break
            
        # 第7回合加入特殊奖励
        if 你共猜了 == 7:
            列表奖励.append("连续不开不再受限")
            列表奖励.append("接下来开失败了消耗机会而不开失败了游戏则失败")
            列表奖励.append("免疫一次中弹死亡")
        if 连续不开不再受限:
            连续不开 = 0    
        if not 连续不开不再受限:
            print(f"你以连续不开{连续不开}次\n你目前最多连续不开{最多连续不开几次}次")
            
        if 难度上升 >= 3:
            难度上升 = 0
            f值 += 1
            值 += 1
            
        print(f"你还有{机会}次机会")
        判断 = random.randint(1,值)
        输入 = input("开还是不开？")
        
        if 连续不开 >= 最多连续不开几次:
            print("输入以被改为开")
            输入 = "开"
            
        # 核心游戏逻辑
        if 输入 == "开" and 判断 <= f值:
            if 开关反转:
                print("你不应该选择开的，你失去了一次机会😵")
                机会 -= 1
                猜测了第几次 += 1
                你共猜了 += 1
            else:
                if 免疫中弹死亡次数 > 0:
                    免疫中弹死亡次数 -= 1
                    if 免疫中弹死亡次数 > 0:
                        print("你免疫了一次中弹死亡😨你还剩{免疫中弹死亡次数}次免疫中弹死亡的机会")
                    else:
                        print("你免疫了一次中弹死亡你没有免疫中弹死亡次数了😨")
                else:            
                    print("😵你输了")
                    你共猜了 += 1
                    break
                
        elif 输入 == "不开" and 判断 > f值:
            if 开关反转:
                print("😵你输了")
                你共猜了 += 1
                break
            else:    
                print("你应该选择不开的，你失去了一次机会😵")
                机会 -= 1
                猜测了第几次 += 1
                你共猜了 += 1
                连续不开 += 1
                print(f"机会已变为{机会}次")
                print(f"连续不开以变为{连续不开}次")
                
        elif (输入 == "不开" and 判断 <= f值) or (输入 == "开" and 判断 > f值):
            if 输入 == "不开":
                连续不开 += 1
                print(f"连续不开已变为{连续不开}次")
            elif 输入 == "开":
                连续不开 = 0    
            print("😋👍继续")
            猜测了第几次 += 1    
            你共猜了 += 1
            
            
        if 机会 <= 0:
            print("你没有机会了😵")    
            break
        
        # 奖励轮
        if 猜测了第几次 >= 猜多少次获得奖励:
            机会 += 每次获得奖励时额外获得的机会的数量
            
            # 随机选三个不重复的奖励
            a, b, c = random.sample(列表奖励, 3)
            
            while True:        
                print(f"1. {a}\n2. {b}\n3. {c}\n")        
                输入 = input("请选择上面其中的一个(请输入数字)")
                
                if 输入 == "1":
                    奖励名 = a
                elif 输入 == "2":
                    奖励名 = b
                elif 输入 == "3":
                    奖励名 = c
                else:
                    print("输入错误，请重新输入")        
                    continue
                    
                # 执行奖励效果（调用函数）
                奖励效果[奖励名]()
                if 奖励名 not in ["获得一次机会","将当前的连续不开降为零"]:
                    被动列表.append(奖励名)
                # 一次性奖励用完就删
                if 奖励名 in ["连续不开不再受限", "接下来开失败了消耗机会而不开失败了游戏则失败", "将当前的连续不开降为零"]:
                    列表奖励.remove(奖励名)
                
                break
                
            猜多少次获得奖励 *= 2    
            难度上升 += 1
                
        time.sleep(1)    
        
    print(f"你一共猜了{你共猜了}次🤔")   
    print("这局游戏你获得了以下被动")
    for m in 被动列表:
        print("\n",m)

游戏()